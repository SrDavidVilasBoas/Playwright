  await page.getByRole('button', { name: 'Adicionar lugares recomendados' }).click();
  await page.getByRole('button', { name: 'Não, obrigado' }).click();
  await page.getByRole('button', { name: 'Continuar' }).click();
  await page.getByRole('button', { name: 'Continuar' }).click();
