  test.setTimeout(700000);
  await page.goto('https://www.ryanair.com/pt/pt');
  await page.getByRole('button', { name: 'Sim, concordo' }).click();
  await page.getByPlaceholder('Partida').click();
  await page.getByRole('button', { name: 'Porto' }).click();
  await page.getByRole('button', { name: 'Marrocos' }).click();
  await page.getByRole('button', { name: 'Marraquexe' }).click();
  await page.getByText('1', { exact: true }).nth(1).click();
  await page.getByText('3', { exact: true }).nth(1).click();
  await page.getByLabel('Feito').click();
  await page.getByLabel('Pesquisar').click();
  await page.locator('flight-card-summary').filter({ hasText: '1 lugares restantes com este' }).getByRole('button').click();
  await page.getByRole('button', { name: 'Selecionar' }).click();
